#include <stdio.h>	// The stdio.h library contains input and output functions

int main()
{
	int a = 10;

	// Format specifiers are placeholders for variable data
	printf("a displayed as base 10 intigers: %i\n", a);   // %i is for integers
	printf("a displayed as base 8 intigers: %o\n", a);   // %i is for integers
	printf("a displayed as base 16 intigers: %x\n", a);   // %i is for integers
	printf("a displayed as base 16 intigers: %x\n", a);   // %i is for integers
	printf("a displayed as base 10 unsigned intiger: %u\n", a);   // %i is for integers
	return 0;
}
